import redis
import time


class ProxyPoolMiddleware(object):
    # Not all methods need to be defined. If a method is not defined,
    # scrapy acts as if the spider middleware does not modify the
    # passed objects.
    def __init__(self, ip=''):
        self.ip = ip
        self.start_time = int(time.time())

    def process_request(self, request, spider):
        wait_time = time.time() - self.start_time
        if wait_time > 5:
            self.start_time = int(time.time())
            self.ip = self.get_ip(spider.settings['REDIS_SETTING'])
        if self.ip != '':
            spider.logger.info("获取到 ip:" + self.ip)
            request.meta["proxy"] = "http://" + self.ip
        else:
            spider.logger.info("未获取到ip，休眠5秒")
            time.sleep(5)

    def get_ip(self, config):
        myredis = redis.Redis(host=config['host'], port=config['port'], db=config['db'], password=config['password'])
        try:
            ip = myredis.get("ip")
            ip = ip.decode("utf-8")
            return ip
        except:
            return ''


